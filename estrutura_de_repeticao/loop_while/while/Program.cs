

class Program
{
    /*
    O loop while e um dos 3 loops existentes na estrutura de repetição.

    While significa enquanto e isso vem de o loop repetir um bloco de codigo enquanto o teste logico e verdadeiro.
    */

    static void Main(string[] args)
    {
        int i = 1;
        System.Console.WriteLine("conta de 1 ate 10");

        while (10 >= i)
        {
            System.Console.WriteLine($"valor atual {i}");
            i++;
        }
    }
}
